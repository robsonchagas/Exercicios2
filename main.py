#Calcular valores
valorA = float(input("Digite o valor de A: "))
valorB = float(input("Digite o valor de B: "))
multiplicado = valorA * valorB
dividido = valorA / valorB
adicao = valorA + valorB
subtrai = valorA - valorB
elevado = valorA ** valorB
print("A multiplicado por B é: ", multiplicado)
print("A dividido por B é: ", dividido)
print("A mais B é: ", adicao, "e A menos B é: ", subtrai)
print("A elevado a B é: ", elevado)